package com.daoshan.school.demo.java_23_design_mode.prototype_mode.Prototype;

public abstract class AbstractProperty implements Prototype{

    protected String name;

    protected String code;

    @Override
    public Object clone(){
        Object clone = null;
        try {
            clone = super.clone();
        } catch (CloneNotSupportedException e) {
            e.printStackTrace();
        }
        return clone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
